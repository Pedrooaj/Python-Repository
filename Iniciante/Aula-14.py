# if/else e elif(else if)

while(True):
    entrada =  input('Você quer "entrar" ou "sair": ')
    if entrada == 'entrar':
        print("Você entrou no sistema")
        break
    elif entrada == 'sair': 
        print("Você saiu do sistema")
        break
    else:
        print("Digite um valor válido")
        

